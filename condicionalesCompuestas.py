sueldo1 = int(input("Introduce el sueldo 1:"))
sueldo2 = int(input("Introduce el sueldo 2:"))

# Condicional if / else
if sueldo1 > sueldo2:
    print("El sueldo 1: " + str(sueldo1) + " es mayor que el sueldo 2: " + str(sueldo2))
else:
    print("El sueldo1: " + str(sueldo1) + " es menor que el sueldo 2: " + str(sueldo2))